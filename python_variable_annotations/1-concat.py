#!/usr/bin/env python3

"""
Concatenate two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings.

    Args:
        str1: (str): first string.
        str2: (str): second string.

    Returns:
        string: concatenation of str1 and str2.
    """
    return str1 + str2
